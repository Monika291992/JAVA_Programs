package Loop_Programs;

public class GreaterNumberThreeNumber {

	public static void main(String[] args) {
		
		int a=10, b=20, c=50;
		
		if(a>b && a>c)
		{
			System.out.println(a+ " is a greatest number");
		}
		else if(b>a && b>c)
		{
			System.out.println(b+ " is a greatest number");
		}

	}

}
